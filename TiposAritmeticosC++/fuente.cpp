#include <iostream>;

using namespace std;

int main(){

	cout << "Tamaño de un char: "<< sizeof(char) << "\n";
	cout << "Tamaño de un signed char: "<< sizeof(signed char)<< "\n";
	cout << "Tamaño de un unsigned char: "<< sizeof(unsigned char) << "\n";
	cout << "Tamaño de un signed short int: "<< sizeof(signed short int) << "\n";
	cout << "Tamaño de un signed int: "<< sizeof(signed int) << "\n";
	cout << "Tamaño de un signed long int: "<< sizeof(signed long int) << "\n";
	cout << "Tamaño de un unsigned short int: "<< sizeof(unsigned short int) << "\n";
	cout << "Tamaño de un unsigned int: "<< sizeof(unsigned int) << "\n";
	cout << "Tamaño de un unsigned long int: "<< sizeof(unsigned long int) << "\n";
	cout << "Tamaño de un float: "<< sizeof(float) << "\n";
	cout << "Tamaño de un double: "<< sizeof(double) << "\n";
	cout << "Tamaño de un unsigned long int: "<< sizeof(long double) << "\n";

}
